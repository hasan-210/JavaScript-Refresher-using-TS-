/** comparison Operator */

// ==  ===   !=  !==
console.log(5 == "5");   // => True    |   == compare between two parameters by Value
console.log(5 === "5");  // => False   |   === compare between two parameters by Value and Type


console.log(5 != "5");   // => False  |   === compare between two parameters by Value 
console.log(5 !== "5");  // => True   |   === compare between two parameters by Value and Type


// >   <   >=  <=
const number1 = 30;
const number2 = 50;

console.log(number1 > number2);  // False
console.log(number1 < number2);  // True

